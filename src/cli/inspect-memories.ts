import { loadMemoryRows, pruneNoiseRecords, type SourceId } from "./inspect-memories-data.js";
import { ttyWriteLine, ttyWriteTagged } from "../shared/tty-log-format.js";

export type InspectMemoryOptions = {
  source: SourceId;
  scope?: "user" | "workspace" | "session";
  limit: number;
  contradictionsOnly: boolean;
  json: boolean;
  pruneNoise?: boolean;
};

export async function runInspectMemories(workspaceDir: string, options: InspectMemoryOptions): Promise<void> {
  if (options.pruneNoise) {
    const removed = await pruneNoiseRecords(workspaceDir, options.source);
    ttyWriteTagged("inspect", `pruned autogenerated noise memories: ${removed}`);
  }

  const rows = await loadMemoryRows(workspaceDir, options.source);
  const byId = new Map(rows.map((row) => [row.id, row]));
  const filtered = rows
    .filter((row) => (options.scope ? row.scope === options.scope : true))
    .filter((row) => (options.contradictionsOnly ? Boolean(row.contradictoryTo) : true))
    .sort((a, b) => (a.provenance?.capturedAt ?? "").localeCompare(b.provenance?.capturedAt ?? "") * -1)
    .slice(0, options.limit);

  if (options.json) {
    ttyWriteLine(JSON.stringify(filtered, null, 2));
    return;
  }

  ttyWriteTagged("inspect", `memories (${filtered.length}/${rows.length} shown)`);
  if (!filtered.length) {
    ttyWriteLine("- no matching memory records");
    if (!options.pruneNoise) process.exitCode = 1;
    return;
  }

  for (const row of filtered) {
    ttyWriteLine(`- [${row.from}] ${row.id} scope=${row.scope} confidence=${row.confidence}`);
    ttyWriteLine(`  statement: ${row.statement}`);
    if (row.context?.category) ttyWriteLine(`  category: ${row.context.category}`);
    if (row.context?.tags?.length) ttyWriteLine(`  tags: ${row.context.tags.join(", ")}`);
    if (row.capture?.horizon) ttyWriteLine(`  horizon: ${row.capture.horizon}`);
    if (row.capture?.expiresAt) ttyWriteLine(`  expiresAt: ${row.capture.expiresAt}`);
    if (row.capture?.reason) ttyWriteLine(`  reason: ${row.capture.reason}`);
    if (row.capture?.references?.length) {
      const refs = row.capture.references.map((ref) => `${ref.kind}:${ref.value}`).join("; ");
      ttyWriteLine(`  references: ${refs}`);
    }
    ttyWriteLine(`  captured: ${row.provenance?.capturedAt ?? "unknown"} source=${row.provenance?.source ?? "unknown"}`);
    if (row.contradictoryTo) {
      const target = byId.get(row.contradictoryTo);
      ttyWriteLine(`  contradictoryTo: ${row.contradictoryTo}${target ? ` -> ${target.statement}` : ""}`);
    }
  }
}